
class Heure{
	private int heures;
	private int minutes;

	Heure( int heures , int minutes){
		this.heures = heures;
		this.minutes = minutes;
		
		
	}
	
	public int getHeures(){
		return heures;
	}
	
	public int minutes(){
		return minutes;
	}
	public void setHeures( int heures){
		this.heures = heures;
	}
	public void setMinutes( int minutes){
		this.minutes = minutes ;
	}
	
}
